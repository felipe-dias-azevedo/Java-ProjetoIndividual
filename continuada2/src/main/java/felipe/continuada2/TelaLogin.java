    private void btEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntrarActionPerformed
        String emailVal = tfEmail.getText();
        String senhaVal = tfSenha.getText();
        
        BasicDataSource dataSource = new BasicDataSource();
        dataSource.setDriverClassName("org.h2.Driver");
        dataSource.setUrl("jdbc:h2:file:./continuada2");
        dataSource.setUsername("sa");
        dataSource.setPassword("");
        
        JdbcTemplate jdbcTemplate = new JdbcTemplate(dataSource);

        List<Usuario> listaUsuarios = jdbcTemplate.query(
                "select * from usuario where email = ? and senha = ?",
                new BeanPropertyRowMapper(Usuario.class), emailVal, senhaVal);
        
        if (!listaUsuarios.isEmpty()) {
            lbResultado.setText(String.format(
                    "Login efetuado com sucesso. Bem vindo(a), %s",
                    listaUsuarios.get(0).getNome()));
            lbResultado.setForeground(Color.blue);
            btEntrar.setEnabled(false);
            
            TelaDadosUsuario telaDados = new TelaDadosUsuario(listaUsuarios.get(0));
            telaDados.setVisible(true);
        } else {
            tentativas--;
            if (tentativas > 0) {
                lbResultado.setText(String.format(
                        "Email e/ou senha errados. VocÃª ainda tem %d tentativas",
                        tentativas));
                lbResultado.setForeground(Color.yellow);
            } else {
                lbResultado.setText("Sistema Bloqueado - Procure o suporte");
                lbResultado.setForeground(Color.red);
                btEntrar.setEnabled(false);
            }
        }
    }//GEN-LAST:event_btEntrarActionPerformed

